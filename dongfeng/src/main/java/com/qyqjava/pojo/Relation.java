package com.qyqjava.pojo;

public class Relation {
    private Integer no;

    private Integer wrehouseId;

    private Integer roadway;

    private Integer shelf;

    private Integer allocationnum;

    public Integer getNo() {
        return no;
    }

    public void setNo(Integer no) {
        this.no = no;
    }

    public Integer getWrehouseId() {
        return wrehouseId;
    }

    public void setWrehouseId(Integer wrehouseId) {
        this.wrehouseId = wrehouseId;
    }

    public Integer getRoadway() {
        return roadway;
    }

    public void setRoadway(Integer roadway) {
        this.roadway = roadway;
    }

    public Integer getShelf() {
        return shelf;
    }

    public void setShelf(Integer shelf) {
        this.shelf = shelf;
    }

    public Integer getAllocationnum() {
        return allocationnum;
    }

    public void setAllocationnum(Integer allocationnum) {
        this.allocationnum = allocationnum;
    }
}