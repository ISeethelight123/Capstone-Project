package com.sq.pojo;

public class Line {

	Integer lineId;
	String lineStartpoint;
	String lineDestination;
	String lineTime;
	String lineTemp1;
	String lineTemp2;
	String lineTemp3;
	
	
	public Integer getLineId() {
		return lineId;
	}
	public void setLineId(Integer lineId) {
		this.lineId = lineId;
	}
	public String getLineStartpoint() {
		return lineStartpoint;
	}
	public void setLineStartpoint(String lineStartpoint) {
		this.lineStartpoint = lineStartpoint;
	}
	public String getLineDestination() {
		return lineDestination;
	}
	public void setLineDestination(String lineDestination) {
		this.lineDestination = lineDestination;
	}
	public String getLineTime() {
		return lineTime;
	}
	public void setLineTime(String lineTime) {
		this.lineTime = lineTime;
	}
	public String getLineTemp1() {
		return lineTemp1;
	}
	public void setLineTemp1(String lineTemp1) {
		this.lineTemp1 = lineTemp1;
	}
	public String getLineTemp2() {
		return lineTemp2;
	}
	public void setLineTemp2(String lineTemp2) {
		this.lineTemp2 = lineTemp2;
	}
	public String getLineTemp3() {
		return lineTemp3;
	}
	public void setLineTemp3(String lineTemp3) {
		this.lineTemp3 = lineTemp3;
	}
	
	public String toString() {
		return "LinePojo [lineId=" + lineId + ", lineStartpoint=" + lineStartpoint + ", lineDestination="
				+ lineDestination + ", lineTime=" + lineTime + ", lineTemp1=" + lineTemp1 + ", lineTemp2=" + lineTemp2
				+ ", lineTemp3=" + lineTemp3 + "]";
	}
	
	

	
	
	
	
	
}
