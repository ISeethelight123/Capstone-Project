package com.sq.pojo;

public class Recipients {
	
	Integer recipientsId;
	String recipientsName;
	String recipientsPhone;
	String recipientsAddress;
	Integer userId;
	String recipientsProvince;
	String provinceCity;
	String provinceRegion;
	
	
	public Integer getRecipientsId() {
		return recipientsId;
	}
	public void setRecipientsId(Integer recipientsId) {
		this.recipientsId = recipientsId;
	}
	public String getRecipientsName() {
		return recipientsName;
	}
	public void setRecipientsName(String recipientsName) {
		this.recipientsName = recipientsName;
	}
	public String getRecipientsPhone() {
		return recipientsPhone;
	}
	public void setRecipientsPhone(String recipientsPhone) {
		this.recipientsPhone = recipientsPhone;
	}
	public String getRecipientsAddress() {
		return recipientsAddress;
	}
	public void setRecipientsAddress(String recipientsAddress) {
		this.recipientsAddress = recipientsAddress;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getRecipientsProvince() {
		return recipientsProvince;
	}
	public void setRecipientsProvince(String recipientsProvince) {
		this.recipientsProvince = recipientsProvince;
	}
	public String getProvinceCity() {
		return provinceCity;
	}
	public void setProvinceCity(String provinceCity) {
		this.provinceCity = provinceCity;
	}
	public String getProvinceRegion() {
		return provinceRegion;
	}
	public void setProvinceRegion(String provinceRegion) {
		this.provinceRegion = provinceRegion;
	}
	
	
	
	
	
	
}
